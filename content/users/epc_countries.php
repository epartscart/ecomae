function epc_countries_dial_prefix(string $countryCode): string
{
	$code = strtoupper(substr(preg_replace('/[^A-Za-z]/', '', $countryCode), 0, 2));
	$map = epc_countries_dial_codes();
	return isset($map[$code]) ? '+' . $map[$code] : '';
}

