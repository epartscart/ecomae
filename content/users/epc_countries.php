/**
 * Address field labels / rules per country.
 *
 * @return array{state_label:string,postal_label:string,postal_required:bool,use_emirate_select:bool,emirates:array<int,string>}
 */
function epc_countries_address_meta(string $countryCode): array
{
	$code = strtoupper(substr(preg_replace('/[^A-Za-z]/', '', $countryCode), 0, 2));
	if ($code === 'AE') {
		return array(
			'state_label' => 'Emirate',
			'postal_label' => 'Postal / ZIP code (optional in UAE)',
			'postal_required' => false,
			'use_emirate_select' => true,
			'emirates' => epc_countries_uae_emirates(),
		);
	}
	if ($code === 'US') {
		return array(
			'state_label' => 'State',
			'postal_label' => 'ZIP code',
			'postal_required' => true,
			'use_emirate_select' => false,
			'emirates' => array(),
		);
	}
	if ($code === 'GB') {
		return array(
			'state_label' => 'County',
			'postal_label' => 'Postcode',
			'postal_required' => true,
			'use_emirate_select' => false,
			'emirates' => array(),
		);
	}
	return array(
		'state_label' => 'State / Province / Region',
		'postal_label' => 'Postal / ZIP code',
		'postal_required' => false,
		'use_emirate_select' => false,
		'emirates' => array(),
	);
}

function epc_countries_normalize_code(string $value): string
{
	$value = trim($value);
	if ($value === '') {
		return '';
	}
	if (preg_match('/^[A-Za-z]{2}$/', $value)) {
		return strtoupper($value);
	}
	$countries = epc_countries_iso3166_alpha2();
	foreach ($countries as $code => $name) {
		if (strcasecmp($name, $value) === 0) {
			return $code;
		}
	}
	if (function_exists('epc_tax_toolkit_country_name_to_iso')) {
		require_once $_SERVER['DOCUMENT_ROOT'] . '/content/shop/finance/epc_tax_toolkit.php';
		$iso = epc_tax_toolkit_country_name_to_iso($value);
		if ($iso !== '') {
			return strtoupper($iso);
		}
	}
	return '';
}
