/** @return array<string, string> Same list with Gulf states pinned after UAE for registration UX */
function epc_countries_registration_options(): array
{
	$all = epc_countries_iso3166_alpha2();
	$gulf = array('AE', 'SA', 'QA', 'KW', 'BH', 'OM');
	$out = array();
	foreach ($gulf as $code) {
		if (isset($all[$code])) {
			$out[$code] = $all[$code];
		}
	}
	foreach ($all as $code => $name) {
		if (!isset($out[$code])) {
			$out[$code] = $name;
		}
	}
	return $out;
}

